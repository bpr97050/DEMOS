c
c
c***************************************************
c       п/п сложения матриц.
c***************************************************
c
c       (а) и (в) -исходные матрицы
c       (с)       -результат
c       na,nb,nc  -номера матриц
c       к         -число строк
c       l         -число столбцов
c
c
	subroutine slmatr(a,b,c,na,nb,nc,k,l)
	dimension a(10,4,4),b(10,4,4),c(10,4,4)
	do 10 i=1,k
	do 10 j=1,l
10      c(nc,i,j)=a(na,i,j)+b(nb,i,j)
	return
	end
